// JavaScript Fundamentals

// Numbers in Javascript

let num1 = 10;

// floating numbers
let num2 = 22.3;
console.log(num2);

// Increment num1 by 1
num1++;

// Decrement num1 by 1
num1--;
console.log(num1);

// Divide, multiply *, remainder %
console.log(num1 % 6); // remainder %

// Increment/decrement  by any number you want  
num1 = num2 + 10;
num1 += 10;
console.log(num1);

//  multiply
num1 *= 2;
console.log(num1);